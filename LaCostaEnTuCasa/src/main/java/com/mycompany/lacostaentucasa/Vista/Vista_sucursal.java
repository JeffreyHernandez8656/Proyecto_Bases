    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        // TODO add your handling code here:
        int id_sucursal=Integer.parseInt(txt_id_sucursal.getText());
        String nombre=txt_nombre.getText();
        int numero_empleados=Integer.parseInt(txt_numero_empleado.getText());
        int id_ciudad=Integer.parseInt(txt_id_ciudad.getText());
        
        Sucursal scr = new Sucursal(id_sucursal, nombre, numero_empleados, id_ciudad);
        Controlador_Sucursal.actualizar_sucursal(scr);
        limpiar_espacios();
        Controlador_Sucursal.cargar_sucursal(tbl_sucursal, modelo_sucursal);
        txt_id_sucursal.setEnabled(true);
    }//GEN-LAST:event_btn_actualizarActionPerformed

