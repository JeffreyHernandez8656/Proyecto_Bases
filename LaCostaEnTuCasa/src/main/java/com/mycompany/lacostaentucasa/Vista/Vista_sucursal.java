    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(JOptionPane.showInputDialog(null, "Escriba el Id Sucursal a eliminar"));
        Controlador_Sucursal.eliminar_sucursal(id, tbl_sucursal, modelo_sucursal);
        Controlador_Sucursal.cargar_sucursal(tbl_sucursal, modelo_sucursal);
    }//GEN-LAST:event_btn_eliminarActionPerformed

