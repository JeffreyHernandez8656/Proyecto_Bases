    private void limpiar_espacios(){
        String t ="";
        txt_id_ciudad.setText(t);
        txt_id_sucursal.setText(t);
        txt_nombre.setText(t);
        txt_numero_empleado.setText(t);
    }
    
    DefaultTableModel modelo_sucursal = new DefaultTableModel();
    private void caragar_modelo_sucursal(){
        modelo_sucursal.addColumn("id_sucursal");
        modelo_sucursal.addColumn("nombre");
        modelo_sucursal.addColumn("numero_empleados");
        modelo_sucursal.addColumn("id_ciudad");
        
        tbl_sucursal.setModel(modelo_sucursal);
    }
    
    private void cargar_datos_txt(){
        int fila_select = tbl_sucursal.getSelectedRow();
        txt_id_sucursal.setText((tbl_sucursal.getValueAt(fila_select, 0)).toString());
        txt_nombre.setText((tbl_sucursal.getValueAt(fila_select, 1)).toString());
        txt_numero_empleado.setText((tbl_sucursal.getValueAt(fila_select, 2)).toString());
        txt_id_ciudad.setText((tbl_sucursal.getValueAt(fila_select, 3)).toString());
        txt_id_sucursal.setEnabled(false);
    }
    
    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        // TODO add your handling code here:
        int id_sucursal=Integer.parseInt(txt_id_sucursal.getText());
        String nombre=txt_nombre.getText();
        int numero_empleados=Integer.parseInt(txt_numero_empleado.getText());
        int id_ciudad=Integer.parseInt(txt_id_ciudad.getText());
        
        Sucursal scr = new Sucursal(id_sucursal, nombre, numero_empleados, id_ciudad);
        Controlador_Sucursal.guardar_sucursal(scr);
        limpiar_espacios();
        Controlador_Sucursal.cargar_sucursal(tbl_sucursal, modelo_sucursal);
    }//GEN-LAST:event_btn_registrarActionPerformed

